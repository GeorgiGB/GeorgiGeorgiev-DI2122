# Exercici 9 fer que imprimisca una llista
# Fes una aplicació que donada la següent llista, imprimisca els seus membres: aficions = [‘esports’, ‘cine’, ‘teatre’]

def main():
    print("Ejercicio 9 Listas")
    # lista de aficions
    aficions = ['esports', 'cine', 'teatre']
    for membre in aficions:
        print(membre)


if __name__ == '__main__':
    main()
